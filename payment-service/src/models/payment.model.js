const sequelize = require('../config/database');
const { DataTypes } = require('sequelize');

/**
 * Payment Model
 * @typedef {object} Payment
 * @property {number} paymentId - Primary key
 * @property {number} ticketId - Associated ticket ID (foreign key from ticket-service)
 * @property {number} passengerId - Associated passenger ID (foreign key from user-service)
 * @property {number} paymentAmount - Amount paid (VND)
 * @property {string} paymentMethod - Payment method (paypal, vnpay, card)
 * @property {string} paymentStatus - Status (PENDING, COMPLETED, FAILED)
 * @property {Date} paymentDate - Date/time of payment
 * @property {object} paymentGatewayResponse - Raw response from payment gateway (JSON)
 */
const Payment = sequelize.define('Payment', {
    /** Payment ID generated by ticket service */
    paymentId: {
        type: DataTypes.STRING,
        primaryKey: true,
        allowNull: false,
        comment: 'Payment ID generated by ticket service (format: TKT_TYPE_TICKETID_PAY_TIMESTAMP)'
    },
    /** Associated ticket ID (foreign key from ticket-service) */
    ticketId: {
        type: DataTypes.INTEGER,
        allowNull: false
    },
    /** Associated passenger ID (foreign key from user-service) */
    passengerId: {
        type: DataTypes.INTEGER,
        allowNull: false
    },
    /** Amount paid (VND) */
    paymentAmount: {
        type: DataTypes.DECIMAL(10, 2),
        allowNull: false
    },
    /** Payment method (MOMO, VNPAY, BANK_TRANSFER, CASH, PAYPAL) */
    paymentMethod: {
        type: DataTypes.ENUM('paypal', 'vnpay', 'card'),
        allowNull: false
    },
    /** Status (PENDING, COMPLETED, FAILED) */
    paymentStatus: {
        type: DataTypes.ENUM('PENDING', 'COMPLETED', 'FAILED'),
        allowNull: false
    },
    /** Date/time of payment */
    paymentDate: {
        type: DataTypes.DATE,
        allowNull: false
    },
    /** Raw response from payment gateway (JSON) */
    paymentGatewayResponse: {
        type: DataTypes.JSON,
        allowNull: true
    }
});

module.exports = Payment;